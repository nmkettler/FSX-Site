              <h3 id="6.4">Section 4 - First Aid Measures</h3>
              <p><strong>Eye Contact:</strong> Remove any contact lenses. Immediately flush eyes with water for at least 15 minutes. Seek medical attention immediately. <br>
                <strong>Skin Contact:</strong> Rinse affected area with water for at least 15 minutes after removing contaminated clothing and shoes. Wash clothing and shoes before reuse. Obtain medical assistance if needed. <br>
                <strong>Ingestion: </strong>If swallowed, do not induce vomiting. Administer artificial respiration by qualified personnel if victim is not breathing. Get medical attention immediately. <br>
                <strong>Inhalation: </strong>If adverse effects occur, remove to an uncontaminated area. Administer artificial respiration by qualified personnel if victim is not breathing. Seek immediate medical attention.<br>
                If breathing is difficult, administer oxygen. If the victim is not breathing, perform mouth-to-mouth resuscitation. <strong>WARNING: </strong>It may be hazardous to the person providing aid to give mouth-to-mouth resuscitation when the inhaled material is toxic, infectious or corrosive. Seek immediate medical attention. </p>
              <h3 id="6.5">Section 5 - Fire Fighting Measures</h3>
              <strong>Extinguishing Media: </strong>Regular dry chemical, Carbon Dioxide, water spray, regular foam. <br>
              <strong>Fire Fighting: </strong>Move container from fire area if possible without risk. Wear full protective clothing. Avoid inhaling any dust generated and wear MSHA/NIOSH approved self-contained breathing apparatus (SCBA). <br>
              <strong>Flash Point:</strong> Not available <br>
              <strong>Auto-ignition Temperature:</strong> Not available <br>
              <strong>Flammability Limits:</strong> Not available <br>
              <strong>Hazardous Combustion Products:</strong> None known <br>
              <h3 id="6.6"><strong>Section 6 - Accidental Release Measures </strong></h3><strong>
              <strong>Exposure Controls: </strong>Ventilate area and do not allow spill to enter drains or watercourses. <br>
              <strong>Personal Protection:</strong> Wear suitable respiratory equipment if airborne dust is generated. Wear non-absorbing gloves suitable for handling chemicals. Use eye protection such as goggles if airborne dust is present. <br>
              <strong>Disposal Considerations:</strong> Damp down and sweep up to avoid generating airborne dust. Use vacuum cleaner. Scoop up and place in sealable container. Dispose in accordance with local regulations.  (See Section 13) <br>
