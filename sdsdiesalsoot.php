              <h3 id="6.3">Section 2 - Hazards Identification</h3>
              <p><strong>Potential Health Hazards (Acute and Chronic): (See Section 11 for more information)</strong> <br>
                <strong> Eye Contact: </strong>Causes eye irritation on direct contact. <br>
                <strong> Skin Contact:</strong> Causes skin irritation upon prolonged contact. <br>
                <strong> Inhalation: </strong>May cause irritation of the respiratory tract. <br>
                <strong> Ingestion:</strong> May be harmful if swallowed. <br>
                <strong> Chronic:</strong> DPM (soot) is listed as a carcinogen/potential carcinogen <br>
                <strong>National Institute for Occupational Safety and Health (NIOSH)</strong>: In 1988, NIOSH found animal evidence for carcinogenesis but limited human evidence. DPM listed as potential occupational carcinogen.<br>
                <strong>International Institute for Research on Cancer (IARC):</strong> In 1989, IARC found evidence for <br>
                carcinogenicity in rats and limited human epidemiology data for carcinogenicity. DPM listed as a probable human carcinogen<br>
  				<strong>National Toxology Program (NTP):</strong> In 2000, NTP found DPM as "reasonably anticipated to be a human <br>
                carcinogen" based on findings of several elevated lung cancer occupational groups exposed to diesel exhaust, in addition to supporting animal studies.<br>
  				<strong>Environmental Protection Agency (EPA):</strong> In 2002, EPA found diesel emissions to be likely <br>
                carcinogens to humans, citing evidence of carcinogenicity of diesel exhaust particles in rats and mice through non-inhalation routes of exposure. <br>
  				<strong>Medical Conditions Generally Known to be Aggravated by Exposure: </strong><br>
                Inhalation of airborne particulate matter may exacerbate asthma symptoms and trigger an asthma attack.<br>
