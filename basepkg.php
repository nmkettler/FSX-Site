                  <h3>Expansion Guide</h3>
                  <p>
                    1. Expand initially with the FSX Extention Ring Kit. The Extention Ringsallows you to process 3 
                        filters at a time.</br></br>

                    2. If more expansion is necessary you can consider adding more TrapBurners™. FSX has found that 
                        about one out of four retrofitted filters needs a thermal cleaning and one out of 6 to 8 active regeneration filters 
                        needs a thermal cleaning. Actual ratio will vary due to numerous factors.</br></br>

                    3. As volume grows you may need to add additional TrapBlaster™ Pneumatic Cleaning machines and 
                        an additional SootSucker Two ™ to collect the ash. Each DPF will release one pint to one quart of ash and soot.</br></br>

                    4. An overhead crane or jib boom may be beneficial in moving filters through the work center 
                        as volume justifies. FSX can provide recommendations.</br></br>

                    5. To get proper cleaning each TrapBlaster™ will need 120 CFM/100 PSI of dry air delivered 
                        to the TrapBlaster running Full Air Mode. Usually this can be accomplished using a 30 to 40 
                        HP air compressor depending on air compressor manufacturer, air dryer used, pipe configuration and altitude.
                        For lower volume shops AirSaver mode requires a minimum of 60 CFM/100 PSI of dry air delivered to the 
                        TrapBlaster, typically with a 20hp or greater compressor.</br></br>
                </p>
